#include <iostream>

/* 
\을 출력하려면 \\이라고 써야 한다.
"을 출력하려면 \"이라고 써야 한다.
'는 상관 없다.
*/

int main()
{
    std::cout << "         ,r'\"7" << std::endl;
    std::cout << "r`-_   ,'  ,/" << std::endl;
    std::cout << " \\. \". L_r'" << std::endl;
    std::cout << "   `~\\/" << std::endl;
    std::cout << "      |" << std::endl;
    std::cout << "      |";
}